#!/usr/bin/env node
/**
 * lint-open-boundary.mjs — open/closed import boundary lint (ratchet mode)
 *
 * Reads export-manifest.json (the explicit open-set path whitelist -- see
 * that file's own "note" field for what it is a seed of) and, for every
 * whitelisted JS/TS/JSX/TSX file, walks its `import`/`export ... from`/
 * literal dynamic `import()`/`require()` specifiers. A relative specifier
 * (starts with "." or "..") that resolves to a repo file NOT in the
 * whitelist is a boundary violation: an "open" file reaching into "closed"
 * territory. Bare specifiers (npm packages, Node builtins) are always
 * skipped -- they are not part of this repo-path classification at all.
 *
 * Scope note: only literal, statically-resolvable specifiers are checked
 * here. A non-literal import()/require()/spawn call in a file that is part
 * of the generated runtime closure is already a hard error at
 * `scripts/compute-cli-closure.mjs` generation time (its own dedicated
 * dynamic-call-site scan) unless explicitly allow-listed there with a
 * reason -- this lint does not re-implement that check. This lint also
 * only understands JS/TS-style import statements, not e.g. an HTML file's
 * `<script src="...">` reference or a CSS `@import` -- those runtime
 * dependencies exist but are outside this fast local guard's scope. That
 * is a deliberate, accepted gap (string/AST-level scanning cannot catch
 * every way a runtime dependency can be expressed): the actual boundary
 * proof is a real build of the open composition plus a spawned CLI smoke
 * test, which is a separate, later, build-level verification step, not
 * this lint. This lint exists to catch the common case fast and locally,
 * in CI, before that heavier proof runs.
 *
 * Ratchet mode: this repository's open/closed boundary is not yet clean --
 * build/open-boundary-baseline.json (generated by
 * scripts/compute-cli-closure.mjs) records the coupling that already
 * exists. A violation already present in that baseline exits 0 with a
 * warning (it is known, tracked debt, not something this lint invented).
 * A violation NOT in the baseline exits non-zero and lists exactly what is
 * new. Once every coupling edge has been broken (a separate, later effort
 * -- this lint's job is only to make the debt visible and stop it from
 * growing), the baseline file should be deleted and this lint switched to
 * absolute mode (zero tolerance, no baseline file, delete the
 * checkRatchet/readBaseline machinery below and fail on any violation at
 * all) -- update this comment when that happens.
 */
import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import ts from "typescript";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
export const REPOSITORY_ROOT = path.resolve(SCRIPT_DIR, "..");

const SCANNABLE_EXTENSIONS = /\.(ts|tsx|js|jsx|mjs|cjs)$/;
const CANDIDATE_EXTENSIONS = [".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs", ".json"];

function toPosix(value) {
  return value.split(path.sep).join("/");
}

// ---------------------------------------------------------------------------
// export-manifest.json loading + directory expansion.
// ---------------------------------------------------------------------------

export function readExportManifest({ rootDir = REPOSITORY_ROOT } = {}) {
  const manifestPath = path.join(rootDir, "export-manifest.json");
  if (!fs.existsSync(manifestPath)) {
    throw new Error(`[lint-open-boundary] export-manifest.json not found at ${manifestPath}`);
  }
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf-8"));
  if (!Array.isArray(manifest.paths)) {
    throw new Error("[lint-open-boundary] export-manifest.json is missing a \"paths\" array.");
  }
  for (const entry of manifest.paths) {
    if (entry.startsWith("!")) {
      throw new Error(
        `[lint-open-boundary] export-manifest.json entry "${entry}" looks like a negation pattern; `
        + "negation patterns are not supported here (explicit whitelist only).",
      );
    }
  }
  return manifest;
}

function listGitTrackedFiles(rootDir, relDir) {
  const output = execFileSync("git", ["ls-files", "--", relDir], { cwd: rootDir, encoding: "utf-8" });
  return output.split("\n").map((line) => line.trim()).filter(Boolean).map(toPosix);
}

/**
 * Expands export-manifest.json's mixed directory/file whitelist into a
 * concrete, deduplicated set of repo-relative file paths. Directory
 * entries (ending in "/") expand to their git-tracked contents only, so
 * gitignored generated output sitting inside an otherwise-whitelisted
 * directory (e.g. a package's own dist/) is never silently swept in.
 */
export function expandManifestPaths({ rootDir = REPOSITORY_ROOT, manifest }) {
  const files = new Set();
  for (const entry of manifest.paths) {
    if (entry.endsWith("/")) {
      const relDir = entry.slice(0, -1);
      const absDir = path.join(rootDir, relDir);
      if (!fs.existsSync(absDir) || !fs.statSync(absDir).isDirectory()) {
        throw new Error(`[lint-open-boundary] export-manifest.json directory entry does not exist: ${entry}`);
      }
      for (const file of listGitTrackedFiles(rootDir, relDir)) files.add(file);
    } else {
      const absPath = path.join(rootDir, entry);
      if (!fs.existsSync(absPath)) {
        throw new Error(`[lint-open-boundary] export-manifest.json file entry does not exist: ${entry}`);
      }
      files.add(entry);
    }
  }
  return files;
}

// ---------------------------------------------------------------------------
// Import-specifier extraction (literal specifiers only -- see module
// docstring for why non-literal ones are out of this lint's scope).
// ---------------------------------------------------------------------------

function scriptKindFor(relPath) {
  if (relPath.endsWith(".tsx")) return ts.ScriptKind.TSX;
  if (relPath.endsWith(".ts")) return ts.ScriptKind.TS;
  if (relPath.endsWith(".jsx")) return ts.ScriptKind.JSX;
  return ts.ScriptKind.JS;
}

/**
 * Collects every literal import/export-from/dynamic-import()/require()
 * specifier in one file's source text. Pure function of (relPath,
 * sourceText) -- fixture-testable without touching the filesystem.
 */
export function collectImportSpecifiers({ relPath, sourceText }) {
  const source = ts.createSourceFile(relPath, sourceText, ts.ScriptTarget.Latest, true, scriptKindFor(relPath));
  const specifiers = [];

  const visit = (node) => {
    if ((ts.isImportDeclaration(node) || ts.isExportDeclaration(node)) && node.moduleSpecifier) {
      if (ts.isStringLiteralLike(node.moduleSpecifier)) {
        const line = source.getLineAndCharacterOfPosition(node.getStart(source)).line + 1;
        specifiers.push({ text: node.moduleSpecifier.text, line });
      }
    } else if (ts.isCallExpression(node)) {
      const isDynamicImport = ts.isImportCall(node);
      const isRequire = ts.isIdentifier(node.expression) && node.expression.text === "require";
      if (isDynamicImport || isRequire) {
        const arg = node.arguments[0];
        if (arg && ts.isStringLiteralLike(arg)) {
          const line = source.getLineAndCharacterOfPosition(node.getStart(source)).line + 1;
          specifiers.push({ text: arg.text, line });
        }
      }
    }
    ts.forEachChild(node, visit);
  };
  visit(source);
  return specifiers;
}

/**
 * Resolves a relative import specifier against its importing file's
 * directory to a concrete repo-relative path, trying the same candidate
 * extensions/index-file order a bundler resolver would. Returns null for
 * non-relative (bare) specifiers -- those are npm packages or Node
 * builtins, never part of this repo-path whitelist check. Throws if a
 * relative specifier cannot be resolved to any file on disk: fail closed
 * rather than silently skip a possibly-real violation.
 */
export function resolveRelativeSpecifier({ rootDir = REPOSITORY_ROOT, importerRelPath, specifierText }) {
  if (!specifierText.startsWith(".")) return null;
  if (path.extname(specifierText) && !CANDIDATE_EXTENSIONS.includes(path.extname(specifierText))) return null;
  const importerDir = path.dirname(importerRelPath);
  const rawTarget = toPosix(path.normalize(path.join(importerDir, specifierText)));

  const candidates = [];
  if (CANDIDATE_EXTENSIONS.some((ext) => rawTarget.endsWith(ext))) {
    candidates.push(rawTarget);
  } else {
    for (const ext of CANDIDATE_EXTENSIONS) candidates.push(`${rawTarget}${ext}`);
    for (const ext of CANDIDATE_EXTENSIONS) candidates.push(`${rawTarget}/index${ext}`);
  }
  for (const candidate of candidates) {
    if (fs.existsSync(path.join(rootDir, candidate))) return candidate;
  }
  throw new Error(
    `[lint-open-boundary] cannot resolve import "${specifierText}" from ${importerRelPath} against any of: `
    + candidates.join(", "),
  );
}

// ---------------------------------------------------------------------------
// Violation detection + ratchet check.
// ---------------------------------------------------------------------------

/**
 * Scans every whitelisted JS/TS/JSX/TSX file for relative imports pointing
 * outside the whitelist. Returns a sorted list of {from, to, line} edges.
 */
export function findBoundaryViolations({ rootDir = REPOSITORY_ROOT, manifest }) {
  const whitelist = expandManifestPaths({ rootDir, manifest });
  const violations = [];
  for (const relPath of [...whitelist].sort()) {
    if (!SCANNABLE_EXTENSIONS.test(relPath)) continue;
    const sourceText = fs.readFileSync(path.join(rootDir, relPath), "utf-8");
    const specifiers = collectImportSpecifiers({ relPath, sourceText });
    for (const { text, line } of specifiers) {
      const resolved = resolveRelativeSpecifier({ rootDir, importerRelPath: relPath, specifierText: text });
      if (resolved === null) continue;
      if (!whitelist.has(resolved)) {
        violations.push({ from: relPath, to: resolved, line });
      }
    }
  }
  violations.sort((a, b) => (
    a.from.localeCompare(b.from) || a.to.localeCompare(b.to) || a.line - b.line
  ));
  return violations;
}

export function readBaseline({ rootDir = REPOSITORY_ROOT } = {}) {
  const baselinePath = path.join(rootDir, "build", "open-boundary-baseline.json");
  if (!fs.existsSync(baselinePath)) {
    throw new Error(
      `[lint-open-boundary] baseline not found at ${baselinePath}; `
      + "run scripts/compute-cli-closure.mjs first.",
    );
  }
  const baseline = JSON.parse(fs.readFileSync(baselinePath, "utf-8"));
  if (!Array.isArray(baseline.edges)) {
    throw new Error("[lint-open-boundary] open-boundary-baseline.json is missing an \"edges\" array.");
  }
  return baseline;
}

function edgeKey(edge) {
  return `${edge.from} -> ${edge.to}`;
}

/**
 * Ratchet comparison: violations already present in the baseline are known
 * debt (ok); anything else is new debt (fail closed).
 */
export function checkRatchet({ violations, baseline }) {
  const baselineKeys = new Set(baseline.edges.map(edgeKey));
  const newViolations = violations.filter((v) => !baselineKeys.has(edgeKey(v)));
  return {
    ok: newViolations.length === 0,
    newViolations,
    knownDebtCount: violations.length - newViolations.length,
    totalViolations: violations.length,
  };
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  const manifest = readExportManifest({});
  const violations = findBoundaryViolations({ manifest });
  const baseline = readBaseline({});
  const result = checkRatchet({ violations, baseline });

  if (result.ok) {
    process.stdout.write(
      `lint:boundary ok -- ${result.totalViolations} known open->closed edge(s), all within the `
      + `tracked ratchet baseline (existing debt, not newly introduced).\n`,
    );
    process.exit(0);
  }

  process.stderr.write(
    `lint:boundary FAILED -- ${result.newViolations.length} open->closed edge(s) outside the tracked `
    + "baseline (new coupling, not previously known):\n"
    + result.newViolations.map((v) => `  ${v.from}:${v.line} -> ${v.to}`).join("\n")
    + `\n(${result.knownDebtCount} other violation(s) are already-known baseline debt, unaffected.)\n`,
  );
  process.exit(1);
}
