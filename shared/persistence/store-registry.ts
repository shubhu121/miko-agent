import type {
  PersistenceExemption,
  PersistenceFormat,
  PersistenceSchemaContract,
  PersistenceSchemaSource,
  PersistenceSiteKind,
  PersistenceSiteRule,
  StartupPhase,
  StoreDescriptor,
} from "./store-registry-types.ts";

const ALL_SITE_KINDS: readonly PersistenceSiteKind[] = [
  "database-open",
  "write-file",
  "append-file",
  "rename",
  "copy-file",
  "mkdir",
  "remove-path",
  "truncate-file",
  "atomic-write",
  "persistent-store-constructor",
];

type StoreInput = {
  id: string;
  ownerModule: string;
  pathPatterns: string[];
  pathExclusions?: string[];
  pathKind?: "file" | "tree";
  format: PersistenceFormat;
  schemaSource: PersistenceSchemaSource;
  openEntry: string[];
  migrationEntry?: string[];
  protocolModules?: string[];
  firstPossibleOpenPhase?: StartupPhase;
  firstPossibleWritePhase?: StartupPhase;
  epochPolicy?: StoreDescriptor["epochPolicy"];
  checkpointPolicy?: string;
  restorePolicy?: string;
  affectedByEpochMigration?: boolean;
  bootstrapSafety?: StoreDescriptor["bootstrapSafety"];
  preCoordinatorReadProjection?: StoreDescriptor["preCoordinatorReadProjection"];
  identityContract: string;
  exemption?: StoreDescriptor["exemption"];
  compatibility?: string;
  siteRules?: PersistenceSiteRule[];
};

function runtimeSource(module: string, contract: string): PersistenceSchemaSource {
  return { kind: "runtime-contract", module, contract };
}

function directorySource(module: string, contract: string): PersistenceSchemaSource {
  return { kind: "directory-contract", module, contract };
}

function rules(
  sourceFiles: string[],
  reason: string,
  kinds: readonly PersistenceSiteKind[] = ALL_SITE_KINDS,
  linePattern?: string,
): PersistenceSiteRule[] {
  return sourceFiles.map((sourceFile) => ({
    sourceFile,
    kinds: [...kinds],
    ...(linePattern ? { linePattern } : {}),
    reason,
  }));
}

function defineStore(input: StoreInput): StoreDescriptor {
  let schemaContract: PersistenceSchemaContract;
  if (input.schemaSource.kind === "narrow-exemption") {
    schemaContract = {
      kind: "exempt",
      source: input.ownerModule,
      compatibility: input.compatibility ?? "No strict reader contract is claimed by this inventory.",
      reason: input.schemaSource.reason,
      expiresOn: input.schemaSource.expiresOn,
    };
  } else {
    schemaContract = {
      kind: input.schemaSource.kind === "directory-contract" ? "protocol" : "runtime-source",
      source: input.schemaSource.kind === "external-versioned"
        ? `${input.schemaSource.packageName} via ${input.schemaSource.lockfile}`
        : input.schemaSource.module,
      compatibility: input.compatibility
        ?? "The named runtime reader is canonical; this inventory does not imply stricter validation than runtime currently performs.",
    };
  }
  return Object.freeze({
    id: input.id,
    ownerModule: input.ownerModule,
    pathPattern: input.pathPatterns[0],
    pathPatterns: [...input.pathPatterns],
    pathExclusions: [...(input.pathExclusions ?? [])],
    pathKind: input.pathKind ?? "file",
    format: input.format,
    schemaSource: input.schemaSource,
    schemaContract,
    openEntry: [...input.openEntry],
    migrationEntry: [...(input.migrationEntry ?? [])],
    protocolModules: [...(input.protocolModules ?? [])],
    firstPossibleOpenPhase: input.firstPossibleOpenPhase ?? "engine_construct",
    firstPossibleWritePhase: input.firstPossibleWritePhase ?? "runtime_ready",
    epochPolicy: input.epochPolicy ?? "epoch-managed",
    checkpointPolicy: input.checkpointPolicy ?? "Included in a future epoch checkpoint unless the coordinator explicitly classifies it otherwise.",
    restorePolicy: input.restorePolicy ?? "Restore only through the owning module after reader compatibility is established.",
    affectedByEpochMigration: input.affectedByEpochMigration ?? true,
    bootstrapSafety: input.bootstrapSafety ?? null,
    preCoordinatorReadProjection: input.preCoordinatorReadProjection ?? null,
    identityContract: input.identityContract,
    exemption: input.exemption ?? null,
    siteRules: [...(input.siteRules ?? [])],
  });
}

